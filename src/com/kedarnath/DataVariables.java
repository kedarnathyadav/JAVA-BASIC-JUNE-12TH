package com.kedarnath;

public class DataVariables {
	
	public static void main(String args[]) {
	//Primitive DataTypes
	int integer_a = 10;
	long long_b = 100000;
	float float_c = 2.3F;
	double double_d = 2.345D;
	short short_e = 2;
	byte byte_d = 1;
	
	//reference DataTypes
	String string_e = "Kedarnath";
	
	System.out.println("Integer = "+ integer_a
			+	"\nLong = " +long_b
			+   "\nFloat = "+float_c
			+   "\nDouble = "+double_d
			+  "\nShort = "+short_e
			+  "\nByte = "+byte_d
			+  "\nString = "+string_e
		
			);
	
	}
	
}
