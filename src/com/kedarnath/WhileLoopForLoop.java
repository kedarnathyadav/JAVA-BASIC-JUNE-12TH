package com.kedarnath;

public class WhileLoopForLoop {
	
	public static void main(String[] args) {
		int i = 1;
	for(int j=1;j<=10;j++) {
		System.out.println("I am in Round no: "+j);
	}
	System.out.println("Welcome to While loop");
	while(i<=10) {
		System.out.println("I am in Round no: "+i);
		i++;
	}
	}
}
